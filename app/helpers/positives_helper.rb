module PositivesHelper

  def pos_count(this_day)
    today=this_day.at_beginning_of_day
    tomorrow=today+24.hours
    positives=Positive.find_by_sql(["select id from positives where updated_at<? and updated_at>?",tomorrow.to_i,today.to_i])
    positives.length
  end
end
