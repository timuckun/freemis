class Setlink < ActiveRecord::Base
  set_primary_key "link_id"
  belongs_to  :pupil_set
  belongs_to :pupil
  has_one :report
  has_many :positives, :order=>"updated_at DESC"
  has_many :interim_report_element_data
  has_many :tracking_element_data
  has_one :merit
end
